int	addition(int a, int b)
{
	return (a + b);
}

int	soustract(int a, int b)
{
	return (a - b);
}

int	mult(int a, int b)
{
	return (a * b);
}

int	divise(int a, int b)
{
	return (a / b);
}

int	mod(int a, int b)
{
	return (a % b);
}
